"use strict"

let x = 10;

x += 2;   //Same as x = x + 2;
console.log(x);
x -= 4;   //Same as x = x - 4;
console.log(x);
x *= 3;   //Same as x = x * 3;
console.log(x);
x /= 6;   //Same as x = x / 6;
console.log(x);
x **= 3;   //Same as x = x ** 3;
console.log(x);
x %= 10;   //Same as x = x % 10;
console.log(x);